import "./App.css";
import type { ReactElement } from "react";
import { BrowserRouter as Router, Routes, Route, Navigate, useLocation } from "react-router-dom";
import MainLayout from "./Layout/main";
import Home from "./pages/Home";
import Payments from "./pages/Payments";
import BOQ from "./pages/BOQ";
import Expenses from "./pages/Expenses";
import Feed from "./pages/Feed";
import Login from "./pages/Login";
import Signup from "./pages/Signup";
import Invite from "./pages/Invite";
import ManageSites from "./pages/ManageSites";
import FeedDetail from "./pages/FeedDetail";
import Landing from "./pages/Landing";
import UserListing from "./pages/UserListing";
import ForgotPassword from "./pages/ForgotPassword";
import ResetPassword from "./pages/ResetPassword";
import AdminSignup from "./pages/AdminSignup";
import Adminpanel from "./pages/Adminpanel";
import { AuthProvider, useAuth } from "./context/AuthContext";
import { SiteProvider } from "./context/SiteContext";

// LoadingScreen removed

const PublicRoute = ({ children }: { children: ReactElement }) => {
  const { token, loading } = useAuth();

  // Removed loading screen

  if (token) {
    return <Navigate to="/home" replace />;
  }

  return children;
};

const RootRedirect = () => {
  const { token, loading } = useAuth();

  // Removed loading screen

  if (token) {
    return <Navigate to="/home" replace />;
  }

  return <Landing />;
};

const ProtectedRoute = ({ children }: { children: ReactElement }) => {
  const { token, loading } = useAuth();

  // Removed loading screen

  if (!token) {
    return <Navigate to="/login" replace />;
  }

  return children;
};

const AdminRoute = ({ children }: { children: ReactElement }) => {
  const { token, loading, user } = useAuth();

  // Removed loading screen

  if (!token) {
    return <Navigate to="/login" replace />;
  }

  if (user?.role !== "ADMIN") {
    return <Navigate to="/" replace />;
  }

  return children;
};

function App() {
  const ResetPasswordCatch: React.FC = () => {
    const location = useLocation();
    if (location.pathname && location.pathname.startsWith("/reset-password")) {
      return <ResetPassword />;
    }
    return <Navigate to="/" replace />;
  };

  return (
    <AuthProvider>
      <SiteProvider>
        <Router>
          <Routes>
            <Route path="/" element={<RootRedirect />} />
            
            <Route 
              path="/login" 
              element={
                <PublicRoute>
                  <Login />
                </PublicRoute>
              } 
            />
            <Route 
              path="/signup" 
              element={
                <PublicRoute>
                  <Signup />
                </PublicRoute>
              } 
            />
            <Route 
              path="/admin-signup" 
              element={
                <PublicRoute>
                  <AdminSignup />
                </PublicRoute>
              }
            />
            <Route path="/forgot-password" element={<ForgotPassword />} />
            <Route path="/reset-password" element={<ResetPassword />} />
            {/* catch any variants like unexpected trailing segments */}
            <Route path="/reset-password/*" element={<ResetPassword />} />

            <Route
              path="/home"
              element={
                <ProtectedRoute>
                  <MainLayout />
                </ProtectedRoute>
              }
            >
              <Route index element={<Home />} />
              <Route path="payments" element={<Payments />} />
              <Route path="boq" element={<BOQ />} />
              <Route path="expenses" element={<Expenses />} />
              <Route path="feed" element={<Feed />} />
              <Route path="feed/:id" element={<FeedDetail />} />
              <Route path="manage-sites" element={<ManageSites />} />
              <Route path="users" element={<UserListing />} />
              <Route
                path="invite"
                element={
                  <AdminRoute>
                    <Invite />
                  </AdminRoute>
                }
              />
            </Route>
            <Route
              path="/adminpanel"
              element={
                <AdminRoute>
                  <Adminpanel />
                </AdminRoute>
              }
            />
            {/* catch-all: render reset-password when pathname begins with it, otherwise redirect home */}
            <Route path="*" element={<ResetPasswordCatch />} />
          </Routes>
        </Router>
      </SiteProvider>
    </AuthProvider>
  );
}

export default App;
